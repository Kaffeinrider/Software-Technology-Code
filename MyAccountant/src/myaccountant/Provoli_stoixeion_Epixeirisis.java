    private void load_Stoixeia_Epixeirisis()
    {
        String query = "";

        query = "SELECT yp_onoma AS 'Όνομα', yp_eponimo AS 'Επώνυμο', meikta AS 'Μεικτά Κέρδη', (meikta - asfalisi) AS 'Καθαρά Κέρδη', asfalisi AS 'Ασφάλιση' FROM ypallilos INNER JOIN epixeirisi ON epixeirisi.username_epixeirisis = ypallilos.yp_username_epixeirisis WHERE epixeirisi.username_epixeirisis = ?;";
        

        try 
        {
            PreparedStatement pst = conn.prepareStatement(query);
            
            pst.setString(1, username); 
            
            ResultSet rs = pst.executeQuery();
            
            DefaultTableModel tblModel = (DefaultTableModel) jTable1.getModel();
            
            tblModel.setRowCount(0); 
            
            while(rs.next())
            {
                String onoma = rs.getString("Όνομα");
                String eponimo = rs.getString("Επώνυμο");
                String meikta = rs.getString("Μεικτά Κέρδη");
                String kathara = String.valueOf(rs.getString("Καθαρά Κέρδη"));
                String asfalisi = String.valueOf(rs.getString("Ασφάλιση"));

                String tbData[] = {onoma , eponimo, meikta, kathara, asfalisi};
          
                tblModel.addRow(tbData); 
            }
        }
        
        catch (SQLException e) 
        {
            JOptionPane.showMessageDialog(this, "Error loading data: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
           }
    }
    
